		public String getFundamentacion() {
			return fundamentacion;
		}
		public void setFundamentacion(String fundamentacion) {
			this.fundamentacion = fundamentacion;
		}
		public String getObjetivo() {
			return objetivo;
		}
		public void setObjetivo(String objetivo) {
			this.objetivo = objetivo;
		}
		public String getDestinatario() {
			return destinatario;
		}
		public void setDestinatario(String destinatario) {
			this.destinatario = destinatario;
		}
		public String getRequisitosIngreso() {
			return requisitosIngreso;
		}
		public void setRequisitosIngreso(String requisitosIngreso) {
			this.requisitosIngreso = requisitosIngreso;
		}
		public Integer getDuracion() {
			return duracion;
		}
		public void setDuracion(Integer duracion) {
			this.duracion = duracion;
		}
		public String getEvaluacion() {
			return evaluacion;
		}
		public void setEvaluacion(String evaluacion) {
			this.evaluacion = evaluacion;
		}
		public String getCorrelatividad() {
			return correlatividad;
		}
		public void setCorrelatividad(String correlatividad) {
			this.correlatividad = correlatividad;
		}
